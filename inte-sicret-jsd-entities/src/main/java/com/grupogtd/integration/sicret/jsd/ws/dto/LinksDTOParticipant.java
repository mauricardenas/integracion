public class LinksDTOParticipant
{
    private String jiraRest;
    private AvatarUrlsDTO avatarUrls;
    private String base;
    private String context;
    private String self;

    public String getJiraRest()
    {

        return jiraRest;
    }

    public void setJiraRest(String jiraRest)
    {
        this.jiraRest = jiraRest;
    }

    public AvatarUrlsDTO getAvatarUrls()
    {
        return avatarUrls;
    }

    public void setAvatarUrls(AvatarUrlsDTO avatarUrls)
    {
        this.avatarUrls = avatarUrls;
    }

    public String getBase()
    {
        return base;
    }

    public void setBase(String base)
    {
        this.base = base;
    }

    public String getContext()
    {
        return context;
    }

    public void setContext(String context)
    {
        this.context = context;
    }

    public String getSelf()
    {
        return self;
    }

    public void setSelf(String self)
    {
        this.self = self;
    }

    @Override
    public String toString()
    {
        return "LinksDTOParticipant{" +
                "jiraRest='" + jiraRest + '\'' +
                ", avatarUrls=" + avatarUrls +
                ", base='" + base + '\'' +
                ", context='" + context + '\'' +
                ", self='" + self + '\'' +
                '}';
    }
}
