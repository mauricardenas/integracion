package com.grupogtd.integration.sicret.jsd.ws.dto;

import java.io.Serializable;

/**
 * Created by softtek on 26/03/2019.
 */
public class ErrorNotificacionDTO implements Serializable
{

    private String proceso;
    private String tipoAfectado;
    private String identificador;
    private String error;
    private String detalleError;

    public String getProceso()
    {
        return proceso;
    }

    public void setProceso(String proceso)
    {
        this.proceso = proceso;
    }

    public String getTipoAfectado()
    {
        return tipoAfectado;
    }

    public void setTipoAfectado(String tipoAfectado)
    {
        this.tipoAfectado = tipoAfectado;
    }

    public String getIdentificador()
    {
        return identificador;
    }

    public void setIdentificador(String identificador)
    {
        this.identificador = identificador;
    }

    public String getError()
    {
        return error;
    }

    public void setError(String error)
    {
        this.error = error;
    }

    public String getDetalleError()
    {
        return detalleError;
    }

    public void setDetalleError(String detalleError)
    {
        this.detalleError = detalleError;
    }

    @Override
    public String toString()
    {
        return "ErrorNotificacionCVDTO{" + "proceso='" + proceso + '\'' + ", tipoAfectado='" + tipoAfectado + '\'' + ", identificador='" + identificador + '\'' + ", error='" + error + '\'' + ", detalleError='" + detalleError + '\'' + '}';
    }
}
