package com.grupogtd.integration.sicret.jsd.ws.request;

import java.io.Serializable;

/**
 * Created by Softtek on 15-03-2019.
 */
public class AddCommentTicketRequest implements Serializable

{
    private String idBitacora;
    private String metodo;
    private String idAplicacion;
    private String tipoEvento;

    public String getTipoEvento()
    {
        return tipoEvento;
    }

    public void setTipoEvento(String tipoEvento)
    {
        this.tipoEvento = tipoEvento;
    }

    public String getMetodo()
    {
        return metodo;
    }

    public void setMetodo(String metodo)
    {
        this.metodo = metodo;
    }

    public String getIdBitacora()
    {
        return idBitacora;
    }

    public void setIdBitacora(String idBitacora)
    {
        this.idBitacora = idBitacora;
    }

    public String getIdAplicacion()
    {
        return idAplicacion;
    }

    public void setIdAplicacion(String idAplicacion)
    {
        this.idAplicacion = idAplicacion;
    }

    @Override
    public String toString()
    {
        return "AddCommentTicketRequest{" +
                "idBitacora='" + idBitacora + '\'' +
                ", metodo='" + metodo + '\'' +
                ", idAplicacion='" + idAplicacion + '\'' +
                ", tipoEvento='" + tipoEvento + '\'' +
                '}';
    }
}
