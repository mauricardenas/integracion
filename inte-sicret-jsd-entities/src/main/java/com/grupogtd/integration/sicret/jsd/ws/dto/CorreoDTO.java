public class CorreoDTO
{
    public CorreoDTO()
    {
    }

    public CorreoDTO(String correo)
    {
        this.correo = correo;
    }

    private String correo;

    public String getCorreo()
    {
        return correo;
    }

    public void setCorreo(String correo)
    {
        this.correo = correo;
    }

    @Override
    public String toString()
    {
        return "CorreoDTO{" +
                "correo='" + correo + '\'' +
                '}';
    }
}
