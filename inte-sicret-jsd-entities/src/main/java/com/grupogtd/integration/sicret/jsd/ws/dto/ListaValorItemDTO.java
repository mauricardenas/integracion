package com.grupogtd.integration.sicret.jsd.ws.dto;

import java.io.Serializable;

public class ListaValorItemDTO implements Serializable
{
    private String tipoLista;
    private String subTipoLista;
    private String valorVisible;
    private String valorNoVisible;
    private String estado;

    public ListaValorItemDTO(String tipoLista, String subTipoLista, String valorVisible, String valorNoVisible, String estado)
    {
        this.tipoLista = tipoLista;
        this.subTipoLista = subTipoLista;
        this.valorVisible = valorVisible;
        this.valorNoVisible = valorNoVisible;
        this.estado = estado;
    }

    public String getTipoLista()
    {
        return tipoLista;
    }

    public void setTipoLista(String tipoLista)
    {
        this.tipoLista = tipoLista;
    }

    public String getSubTipoLista()
    {
        return subTipoLista;
    }

    public void setSubTipoLista(String subTipoLista)
    {
        this.subTipoLista = subTipoLista;
    }

    public String getValorVisible()
    {
        return valorVisible;
    }

    public void setValorVisible(String valorVisible)
    {
        this.valorVisible = valorVisible;
    }

    public String getValorNoVisible()
    {
        return valorNoVisible;
    }

    public void setValorNoVisible(String valorNoVisible)
    {
        this.valorNoVisible = valorNoVisible;
    }

    public String getEstado()
    {
        return estado;
    }

    public void setEstado(String estado)
    {
        this.estado = estado;
    }

    @Override
    public String toString()
    {
        return "ListaValorItemDTO{" +
                "tipoLista='" + tipoLista + '\'' +
                ", subTipoLista='" + subTipoLista + '\'' +
                ", valorVisible='" + valorVisible + '\'' +
                ", valorNoVisible='" + valorNoVisible + '\'' +
                ", estado='" + estado + '\'' +
                '}';
    }
}
